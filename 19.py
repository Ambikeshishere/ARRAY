# WAP to check if a number is a multiple of 7 or not.

num = input("Ae beta chal number bata jaldi se :- \n")
num = float(num)
if (num % 7 == 0):
    print(" Number 7 se divide ho jaayega.\n This Number is a multiple of 7\n")
else:
    print("Ye to 7 se divide na hoga.\n This is not a multiple of 7\n")



